package Ajedrez;

public class Posicion {

	protected int fila;
	protected int columna;
	
	public Posicion(){
		fila = 0;
		columna = 0;
	}
	
	public int getfila(){
		return fila;
	}
	public int columna(){
		return columna;
	}
	
	public void setfila(int fila){
		this.fila = fila;
	}
	public void setcolumna(int columna){
		this.columna = columna;
	}
	
	
	
	
	
	
	
	
	
}
